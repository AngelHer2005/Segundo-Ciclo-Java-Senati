    private void calculateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_calculateActionPerformed
        // TODO add your handling code here:
        if (!exam.getText().isEmpty() && !practice.getText().isEmpty() && !notbook.getText().isEmpty()){
        examen = Integer.parseInt(exam.getText()); practica = Integer.parseInt(practice.getText()); cuaderno = Integer.parseInt(notbook.getText());validor=true;
        }else{JOptionPane.showMessageDialog(null,"Te falta rellenar las notas.");}
        
        if(validor==true && (examen>=0 && examen<=20) && (practica >=0 && practica<=20) && (cuaderno>=0 && cuaderno<=20)){
        if(name.getText().isEmpty() && Lname.getText().isEmpty() && course.getText().isEmpty()){
            JOptionPane.showMessageDialog(null,"Te falta rellenar todo.");
        }else{if(name.getText().isEmpty()){
            JOptionPane.showMessageDialog(null,"Te falta rellenar el nombre.");
        }else{if(Lname.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Te falta rellenar los apellidos.");
        }else{if(course.getText().isEmpty()){
            JOptionPane.showMessageDialog(null,"Te falta rellenar el curso");
        }else{
            prom.setText(String.valueOf((examen+practica+cuaderno)/3));validor=false;
        }}}}}
        else{if(!exam.getText().isEmpty() && !practice.getText().isEmpty() && !notbook.getText().isEmpty()){
            JOptionPane.showMessageDialog(null,"Ingresa notas v√°lidas.");
        }}
    }//GEN-LAST:event_calculateActionPerformed

